// ---------------------------------------------------------------------------
// generateFiscalAttestation - Create a fiscal attestation for a client/year
// ---------------------------------------------------------------------------
export async function generateFiscalAttestation(
  clientId: string,
  year: number,
): Promise<FiscalAttestation> {
  // 1. Fetch all paid invoices for this client in the given fiscal year
  //    that are eligible for tax credit
  const { data: invoices, error: invoicesError } = await supabase
    .from('invoices')
    .select('*')
    .eq('client_id', clientId)
    .eq('eligible_tax_credit', true)
    .eq('status', 'payee')
    .gte('issue_date', `${year}-01-01`)
    .lte('issue_date', `${year}-12-31`)

  if (invoicesError) throw invoicesError

  const invoiceList = invoices ?? []

  // 2. Calculate aggregated amounts
  const totalAmountTtc = invoiceList.reduce((sum, inv) => sum + Number(inv.total_ttc), 0)
  const totalLaborHt = invoiceList.reduce((sum, inv) => sum + Number(inv.labor_amount_ht), 0)
  const taxCreditAmount = invoiceList.reduce((sum, inv) => sum + Number(inv.tax_credit_amount), 0)
  const invoiceIds = invoiceList.map((inv) => inv.id)

  // 3. Fetch company settings (use defaults if not found)
  const { data: settings } = await supabase
    .from('settings')
    .select('key, value')
    .in('key', ['company_name', 'company_siret', 'company_address', 'company_agrement'])

  const settingsMap: Record<string, string> = {}
  if (settings) {
    settings.forEach((s) => {
      settingsMap[s.key] = String(s.value ?? '')
    })
  }

  // 4. Upsert the attestation (unique per client + fiscal year)
  const attestationData = {
    reference: `ATT-${year}-${clientId.substring(0, 8).toUpperCase()}`,
    client_id: clientId,
    fiscal_year: year,
    total_amount_ttc: totalAmountTtc,
    total_labor_ht: totalLaborHt,
    tax_credit_amount: taxCreditAmount,
    invoice_ids: invoiceIds,
    company_name: settingsMap['company_name'] ?? brand.name.split(' ')[0],
    company_siret: settingsMap['company_siret'] ?? '',
    company_address: settingsMap['company_address'] ?? '',
    company_agrement: settingsMap['company_agrement'] ?? null,
  }

  const { data, error } = await supabase
    .from('fiscal_attestations')
    .upsert(attestationData, { onConflict: 'client_id,fiscal_year' })
    .select()
    .single()

  if (error) throw error
  return data as FiscalAttestation
}

