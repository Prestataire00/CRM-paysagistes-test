// ---------------------------------------------------------------------------
// convertQuoteToInvoice - Accept quote and auto-generate invoice
// ---------------------------------------------------------------------------
export async function convertQuoteToInvoice(quoteId: string): Promise<{
  quote: Quote
  invoice: Invoice
}> {
  const quoteWithLines = await getQuote(quoteId)

  if (!quoteWithLines.client_id) {
    throw new Error('Le devis doit être associé à un client pour générer une facture.')
  }

  if (quoteWithLines.converted_to_invoice_id) {
    throw new Error('Ce devis a déjà été converti en facture.')
  }

  const today = new Date().toISOString().split('T')[0]

  // Fetch client payment config
  const { data: client } = await supabase
    .from('clients')
    .select('payment_terms_days, default_payment_method')
    .eq('id', quoteWithLines.client_id)
    .single()

  const paymentDays = client?.payment_terms_days ?? 30
  const dueDate = new Date()
  dueDate.setDate(dueDate.getDate() + paymentDays)

  // Calculate labor amount
  const laborAmountHt = (quoteWithLines.lines ?? [])
    .filter((line: QuoteLine) => line.is_labor)
    .reduce((sum: number, line: QuoteLine) => sum + Number(line.total_ht), 0)

  // Build invoice from quote
  const invoiceData = {
    client_id: quoteWithLines.client_id,
    quote_id: quoteId,
    chantier_id: null,
    title: quoteWithLines.title,
    description: quoteWithLines.description,
    status: 'emise' as InvoiceStatus,
    issue_date: today,
    due_date: dueDate.toISOString().split('T')[0],
    paid_date: null,
    subtotal_ht: quoteWithLines.subtotal_ht,
    tva_rate: quoteWithLines.tva_rate,
    tva_amount: quoteWithLines.tva_amount,
    total_ttc: quoteWithLines.total_ttc,
    discount_percentage: quoteWithLines.discount_percentage,
    discount_amount: quoteWithLines.discount_amount,
    amount_paid: 0,
    payment_method: (client?.default_payment_method ?? null) as Invoice['payment_method'],
    payment_reference: null,
    eligible_tax_credit: quoteWithLines.eligible_tax_credit,
    labor_amount_ht: laborAmountHt,
    tax_credit_amount: quoteWithLines.tax_credit_amount,
    net_after_credit: quoteWithLines.net_after_credit,
    is_archived: false,
    pdf_url: null,
    exported_at: null,
    accounting_reference: null,
    created_by: null,
  }

  // Build invoice lines from quote lines
  const invoiceLines = (quoteWithLines.lines ?? []).map((line: QuoteLine) => ({
    description: line.description,
    quantity: line.quantity,
    unit: line.unit,
    unit_price_ht: line.unit_price_ht,
    tva_rate: line.tva_rate,
    total_ht: line.total_ht,
    total_ttc: line.total_ttc,
    is_labor: line.is_labor,
    sort_order: line.sort_order,
  }))

  // Create invoice
  const invoice = await createInvoice(invoiceData, invoiceLines)

  // Update quote as accepted + link to invoice
  const { data: updatedQuote, error: updateError } = await supabase
    .from('quotes')
    .update({
      status: 'accepte',
      accepted_date: today,
      converted_to_invoice_id: invoice.id,
    })
    .eq('id', quoteId)
    .select()
    .single()

  if (updateError) throw updateError

  return {
    quote: updatedQuote as Quote,
    invoice,
  }
}

