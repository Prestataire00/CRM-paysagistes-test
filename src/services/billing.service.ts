// ---------------------------------------------------------------------------
// Eligible client for attestation (has paid invoices with tax credit in year)
// ---------------------------------------------------------------------------
export interface EligibleClient {
  client_id: string
  first_name: string
  last_name: string
  company_name: string | null
  email: string | null
  address_line1: string
  postal_code: string
  city: string
  invoice_count: number
  total_ttc: number
  total_labor_ht: number
}

export async function getEligibleClientsForAttestation(year: number): Promise<EligibleClient[]> {
  // Get all paid invoices with tax credit for the year
  const { data: invoices, error } = await supabase
    .from('invoices')
    .select('client_id, total_ttc, labor_amount_ht')
    .eq('eligible_tax_credit', true)
    .eq('status', 'payee')
    .gte('issue_date', `${year}-01-01`)
    .lte('issue_date', `${year}-12-31`)

  if (error) throw error
  if (!invoices || invoices.length === 0) return []

  // Group by client_id
  const byClient = new Map<string, { count: number; totalTtc: number; totalLaborHt: number }>()
  for (const inv of invoices) {
    const existing = byClient.get(inv.client_id) ?? { count: 0, totalTtc: 0, totalLaborHt: 0 }
    existing.count++
    existing.totalTtc += Number(inv.total_ttc)
    existing.totalLaborHt += Number(inv.labor_amount_ht)
    byClient.set(inv.client_id, existing)
  }

  // Fetch client details
  const clientIds = [...byClient.keys()]
  const { data: clients, error: clientError } = await supabase
    .from('clients')
    .select('id, first_name, last_name, company_name, email, address_line1, postal_code, city')
    .in('id', clientIds)

  if (clientError) throw clientError

  return (clients ?? []).map((c) => {
    const agg = byClient.get(c.id)!
    return {
      client_id: c.id,
      first_name: c.first_name,
      last_name: c.last_name,
      company_name: c.company_name,
      email: c.email,
      address_line1: c.address_line1,
      postal_code: c.postal_code,
      city: c.city,
      invoice_count: agg.count,
      total_ttc: agg.totalTtc,
      total_labor_ht: agg.totalLaborHt,
    }
  })
}

